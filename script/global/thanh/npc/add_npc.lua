function add_boss(TabIndex)
    -- local nNpc = getn(TabIndex)
    for i = 1, getn(TabIndex) do
        local nID = TabIndex[i][1]
        local nLevel = TabIndex[i][2]
        local nSeries = random(0, 4)
        local nSubWorld = SubWorldID2Idx(TabIndex[i][3])
        local nX = TabIndex[i][4] * 32
        local nY = TabIndex[i][5] * 32
        local nRevive = TabIndex[i][6]
        local nName = TabIndex[i][7]
        local nIsBoss = TabIndex[i][8]
        local nScript = TabIndex[i][9]
        if (nSubWorld >= 0) then
            newtasknpcindex = AddNpcEx(nID, nLevel, nSeries, nSubWorld, nX, nY, nRevive, nName, nIsBoss)
            -- newtasknpcindex = AddNpcEx(Tab3[i][1],Tab3[i][2],random(0,4),Tid,TabValue4,TabValue5,Tab3[i][6],Tab3[i][7],Tab3[i][8]);
            --								AddNpcEx( BossId,BossLevel ,BossSeries, SubWorldID2Idx(W), X * 32, (Y +  5) * 32, 1,BossName, 1(isboss) );
            SetNpcDeathScript(newtasknpcindex, nScript);
            SetNpcParam(newtasknpcindex, 1, TabIndex[i][10]);
        end
    end
end

